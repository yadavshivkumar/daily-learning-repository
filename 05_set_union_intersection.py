s1 = {1 , 2, 24, 56, 78, 90 , "Shiv" }

s2 = {1 , 2 , 24, 67, 78, 65, "Shiv" }

print(s1.union(s2))  # Union of two sets

print(s1 | s2)  # Another way to get the union of two sets

print(s1.intersection(s2))  # Intersection of two sets

print(s1 & s2)  # Another way to get the intersection of two sets

print(s1.difference(s2))  # Difference of two sets

print(s1 - s2)  # Another way to get the difference of two sets

print(s1.symmetric_difference(s2))  # Symmetric difference of two sets

print(s1 ^ s2)  # Another way to get the symmetric difference of two sets

